package ifstatments;

public class switchStatment {

	public static void main(String[] args) {
		
int holiday = 3;
		
		
		switch (holiday) {
		
		case 1: 
			
			System.out.println("New year");
			
			break;
			
		case 2:
			
			System.out.println("Valentains day");
			
			break;
			
		case 3:
			
			System.out.println("Summer break");
			
		case 4: 
			
			System.out.println("Halloween");
			
			break;
			
			

	}
	

	}



	}


